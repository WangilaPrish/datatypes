void main() {
  // Integer data type for age
  int age = 20;

  // Double data type for GPA
  double gpa = 3.8;

  // String data type for student name
  String name = 'Alice Smith';

  // List data type for courses
  List<String> courses = ['Mathematics', 'Computer Science', 'Physics'];

  // Map data type for student details
  Map<String, dynamic> student = {
    'name': 'Priscilla Wangila',
    'age': 20,
    'major': 'Earth and Climate Science',
    'gpa': 3.8,
    'courses': ['Mathematics', 'Meteorology', 'Physics']
  };

  // Printing values to demonstrate the use of data types
  print('Student Name: $name');
  print('Student Age: $age');
  print('Student GPA: $gpa');
  print('Student Courses: $courses');
  print('Student Details: $student');}